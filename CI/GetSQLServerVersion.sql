 SELECT 
   SUSER_NAME() U,
   SYSDATETIME() T,
   @@VERSION V, 
   SERVERPROPERTY('ProductLevel') AS L,
   SERVERPROPERTY('ProductVersion') AS N,
   CASE (SELECT PARSENAME(XX,4)+'.'+PARSENAME(XX,3) FROM (SELECT (CAST(SERVERPROPERTY('ProductVersion') AS NVARCHAR(MAX)))XX)X)
     WHEN '9.0' THEN 'SQL2005'
     WHEN '10.0' THEN 'SQL2008'
     WHEN '10.50' THEN 'SQL2008R2'
     WHEN '11.0' THEN 'SQL2012'
     WHEN '12.0' THEN 'SQL2014'
     WHEN '13.0' THEN 'SQL2016'
     WHEN '14.0' THEN 'SQL2017'
     WHEN '15.0' THEN 'SQL2019'
     ELSE 'Unknown'
    END S
